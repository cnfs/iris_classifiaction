    void addRecord()
    {
        int p = JOptionPane.showConfirmDialog(null, "Are you sure you want to add record?","Add Record",JOptionPane.YES_NO_OPTION);
        if(p==0){ 
			if(txt_id.getText().length()==0||txt_username.getText().length()==0||txt_password.getText().length()==0)
			{
				 JOptionPane.showMessageDialog(null,"Insufficient data to add user","Message",JOptionPane.DEFAULT_OPTION);
				 return;
			}

        try {

            String sql ="select * from users where emp_id=? ";
             pst=conn.prepareStatement(sql);
            pst.setString(1,txt_id.getText());
            rs=pst.executeQuery();
            if(rs.next())
            {
                JOptionPane.showMessageDialog(null,"Record already present!!","Message",JOptionPane.DEFAULT_OPTION);
		return;
            }
            rs.close();
            pst.close();
            
            
            
            
            sql ="insert into Users (username,password,emp_id) values (?,?,?) ";

            pst=conn.prepareStatement(sql);
            pst.setString(1,txt_username.getText());
            pst.setString(2,txt_password.getText());
            pst.setString(3,txt_id.getText());

            pst.execute();
            JOptionPane.showMessageDialog(null,"Record is saved successfully","Message",JOptionPane.DEFAULT_OPTION);

        }
        catch (Exception e)

        {
            
        }
        finally {

            try{
                rs.close();
                pst.close();

            }
            catch(Exception e){

            }
        }
        Fillcombo();
        }
      

    }
    void  deleteRecord()
    {
          int p = JOptionPane.showConfirmDialog(null, "Are you sure you want to delete record?","Delete",JOptionPane.YES_NO_OPTION);
          
        if(p==0){
            if(txt_username.getText().equalsIgnoreCase("Admin")&&txt_id.getText().equals("0"))
          {
                JOptionPane.showMessageDialog(null,"Admin can never be deleted","Mesage",JOptionPane.DEFAULT_OPTION);
                return;
          }
         String sql ="delete from Users where username=? ";
        try{
            pst=conn.prepareStatement(sql);
            pst.setString(1, txt_username.getText());
            pst.execute();
            JOptionPane.showMessageDialog(null,"Record Deleted","Mesage",JOptionPane.DEFAULT_OPTION);
            
            
        }catch(Exception e){
            
            JOptionPane.showMessageDialog(null, e);
        }finally {
            
            try{
                rs.close();
                pst.close();
                
            }
            catch(Exception e){
                
            }
        }
        Fillcombo();
        clear();
        
        }
        
        
    }
